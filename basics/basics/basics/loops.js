// For loop: count from 1 to 5
for (let i = 1; i <= 5; i++) {
  console.log("Count: " + i);
}

// While loop: countdown from 5 to 1
let countdown = 5;
while (countdown > 0) {
  console.log("Countdown: " + countdown);
  countdown--;
}

// For...of loop: list hobbies
let hobbies = ["coding", "reading", "gaming"];
for (let hobby of hobbies) {
  console.log("Hobby: " + hobby);
}

